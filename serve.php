<div class="title"><h2>The Serve</h2></div>

 	    	A great serve takes much practice. But once you master it, well, master it a bit, you'll find that it is your secret weapon. It can be the shot that you rely on. In club play, great serves are not terribly common. Many club players learned tennis on their own and learned lots of bad habits that are tough to break.<br>
 	    	There are a few key things to master when working on your serve:<br>
 	    	<ul>
 	    		<li>Keep the toss high and in front of you</li>
				<li>Drop the racquet behind you when your stroke reaches its maximum height on the wind up</li>
 	    		<li>Do not choke up on the grip. Keep your grip a little loose and try to keep your pinkie finger practically hanging off the butt of the grip.</li>
 	    		<li>Come over and down on the ball at the height of its ascent</li>
 	    		<li>Snap your wrist on contact. In other words, there are two motions involved, one with the swing of the arm, the other with the snap of the wrist.</li>
 	    		<li>Keep your opposite hand pointing up at the ball</li>
 	    		<li>Turn you left shoulder, if you are right handed, towards the net</li>
 	    		<li>Left foot (opposite foot) is in front</li>
 	    	</ul>
 	    	<br>The grip, which is often discussed, is almost never discussed in terms of the butt of the racquet. Players are ofteen instructed on which grip to use: continental, western, etc. However, the critical piece is <img id="grip" src="images/grip.jpg" alt="tennis">almost never mentioned, and that is keeping your hand very far down on the racquet grip. Note the grip Rafael Nadal uses in the photo. His hand is hanging over the end of the racquet. This allows your swing to snap and allow much more fexibility in your swing.
 	    	Give it a try next time you're on the court. Try this technique on a number of strokes. You may find you have more power, more reach, and many other benefits.

